# Usos principales de la funcion print()

print("este es un ejemplo basico")

# A la funcion print tambien podemos pasarle parametros

# uno de los parametros es end con este podemos indicarle a la consola como va a terminar la imprension

# En este ejemplo las dos lineas se imprimiran en una sola linea y con un espacio entre la dos porque asi se lo hemos indicado

print("primera linea", end=" ")
print("segunda linea")


#otro parametro es el sep este nos permitira indicar como queremos que se separen los textos

print("uno", "dos", "tres", sep="-")





